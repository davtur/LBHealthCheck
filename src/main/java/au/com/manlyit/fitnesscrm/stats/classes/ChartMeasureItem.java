package au.com.manlyit.fitnesscrm.stats.classes;

/**
 *
 * @author david
 */
public class ChartMeasureItem  {
    
    
    private String timeTaken;
    private float waist;
    private float hips;
    private float thighs;
    private float chest;
    private float arms;
  /**
     * @return the timeTaken
     */
    public String getTimeTaken() {
        return timeTaken;
    }

    /**
     * @param timeTaken the timeTaken to set
     */
    public void setTimeTaken(String timeTaken) {
        this.timeTaken = timeTaken;
    }

    /**
     * @return the waist
     */
    public float getWaist() {
        return waist;
    }

    /**
     * @param waist the waist to set
     */
    public void setWaist(float waist) {
        this.waist = waist;
    }

    /**
     * @return the hips
     */
    public float getHips() {
        return hips;
    }

    /**
     * @param hips the hips to set
     */
    public void setHips(float hips) {
        this.hips = hips;
    }

    /**
     * @return the thighs
     */
    public float getThighs() {
        return thighs;
    }

    /**
     * @param thighs the thighs to set
     */
    public void setThighs(float thighs) {
        this.thighs = thighs;
    }

    /**
     * @return the chest
     */
    public float getChest() {
        return chest;
    }

    /**
     * @param chest the chest to set
     */
    public void setChest(float chest) {
        this.chest = chest;
    }

    /**
     * @return the arms
     */
    public float getArms() {
        return arms;
    }

    /**
     * @param arms the arms to set
     */
    public void setArms(float arms) {
        this.arms = arms;
    }


}
